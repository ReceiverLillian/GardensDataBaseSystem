package bean;

public class GenusBean {
    private String genus_name;
    private int genus_id;


    public void setGenus_id(int genus_id) {
        this.genus_id = genus_id;
    }

    public void setGenus_name(String genus_name) {
        this.genus_name = genus_name;
    }

    public String getGenus_name() {
        return genus_name;
    }

    public int getGenus_id() {
        return genus_id;
    }
}
